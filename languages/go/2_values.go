package main

import "fmt"

func main() {
    
    // String concatenation
    fmt.Println("go" + "lang")
    
    // Integers and floats
    fmt.Println("1+1 =", 1+1)
    fmt.Println("7.0/3.0 =", 7.0/3.0)

    // Booleans
    fmt.Println(true && false)
    fmt.Println(true || false)
    fmt.Println(!true)
}