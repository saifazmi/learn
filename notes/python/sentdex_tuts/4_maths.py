# Basic Maths in Python

# Add
print(1+3)

# Sub
print(3-1)

# Multiplication
print(4*4)

# Division
print(5/4)

'''
' Since python 3 the division operator is overloaded and will return
' a float value even when operating on two whole numbers
' and not round down the answer.
'
' The rounded value can still be achieved by using the // operator
'''

# Flooring Division with Integer output i.e. rounded down always
print(5//4)

# Outputs the remainder of the division
print(2%2)

# Exponent
print (4**4)
